"""xArm7 (7-DoF) no-gripper agent for pg3d reach.

ManiSkill bundles a ``xarm7_ability`` agent (xArm7 + Ability hand) but no bare
no-gripper xArm7, while the hardware target is a gripper-less xArm7. The bundled
``xarm7.urdf`` (PACKAGE_ASSET_DIR, no asset download) is the bare 7-DoF arm, so we
register a no-gripper agent over it, mirroring ManiSkill's own ``xarm6_nogripper``.

TCP is ``link_eef`` (the fixed flange link at the end of ``link7``); a ``tcp_pose``
property is added because — unlike Panda — ManiSkill's no-gripper agents only set
``self.tcp`` and the pg3d reach env reads ``agent.tcp_pose``.
"""

