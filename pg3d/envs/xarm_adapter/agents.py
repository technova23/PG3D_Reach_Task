# Combined xArm7 + Robotiq 2F-85 URDF, generated by
# dataset_generation/build_xarm7_robotiq_urdf.py (absolute mesh paths, so location
# is irrelevant to mesh resolution). Lives beside this module under assets/.
_XARM7_ROBOTIQ_URDF = str(Path(__file__).resolve().parent / "assets" / "xarm7_robotiq.urdf")


def _build_xarm7_gripper_colored_urdf() -> str:
    """Generate a colored xarm7_with_gripper URDF and return its absolute path.

    Copies the ManiSkill-bundled URDF into assets/ and injects URDF material color
    tags so that:
      - xarm_gripper_base_link → white  (the boxy housing)
      - all knuckle / finger links      → black
      - arm links (link_base … link7)   → unchanged (GLB meshes have baked colors)

    Mesh paths are kept as relative ``meshes/…`` references — mplib resolves them
    relative to the URDF directory, so a symlink ``assets/meshes → <venv>/xarm7/meshes``
    is created beside the output URDF. (mplib ignores absolute paths and prepends the
    URDF dir, causing double-path concatenation — symlinks+relative is required.)

    The file is written once; subsequent imports reuse the cached copy.
    """
    import os
    import xml.etree.ElementTree as ET

    xarm7_dir = Path(PACKAGE_ASSET_DIR) / "robots" / "xarm7"
    src = xarm7_dir / "xarm7_with_gripper.urdf"
    assets_dir = Path(__file__).resolve().parent / "assets"
    dst = assets_dir / "xarm7_with_gripper_colored.urdf"

    # Ensure the meshes symlink exists so relative paths in the URDF resolve for mplib.
    meshes_link = assets_dir / "meshes"
    if not meshes_link.exists() and not meshes_link.is_symlink():
        os.symlink(str(xarm7_dir / "meshes"), str(meshes_link))

    if dst.exists():
        return str(dst)

    tree = ET.parse(src)
    root = tree.getroot()

    # Color map: only gripper links get explicit material tags.
    # STL meshes carry no color; GLB arm meshes have baked-in colors → leave alone.
    _WHITE = "0.95 0.95 0.95 1.0"
    _BLACK = "0.12 0.12 0.12 1.0"
    color_map = {
        "xarm_gripper_base_link": _WHITE,
        "left_outer_knuckle":     _BLACK,
        "left_finger":            _BLACK,
        "left_inner_knuckle":     _BLACK,
        "right_outer_knuckle":    _BLACK,
        "right_finger":           _BLACK,
        "right_inner_knuckle":    _BLACK,
    }

    for link in root.iter("link"):
        rgba = color_map.get(link.get("name", ""))
        if rgba is None:
            continue
        mat_name = "white" if rgba == _WHITE else "black"
        for visual in link.findall("visual"):
            mat = ET.SubElement(visual, "material")
            mat.set("name", mat_name)
            color_elem = ET.SubElement(mat, "color")
            color_elem.set("rgba", rgba)

    dst.parent.mkdir(parents=True, exist_ok=True)
    tree.write(str(dst), xml_declaration=True, encoding="utf-8")
    return str(dst)


_XARM7_GRIPPER_COLORED_URDF = _build_xarm7_gripper_colored_urdf()


